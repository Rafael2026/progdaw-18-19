public class UsoDeOperadoresAritmeticos {
  public static void main(String[] args) {
    int x;
    x = 100;
    int resultadoParcial;

    
    resultadoParcial = x + 5;
    x++;
    
    //x = x +1;
    
    
    System.out.println(x + " " + resultadoParcial + " " + (x - 5));
    System.out.println((x * 5) + " " + (x / 5) + " " + (x % 5));
  }
}
